#!/usr/bin/env node

// Regenerates packages/ui-styles/src/globals/font-fallbacks.generated.css.
// Run after adding/removing a font file in packages/ui-styles/src/fonts/.

import { writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { entireMetricsCollection } from '@capsizecss/metrics/entireMetricsCollection';
import { fromFile } from '@capsizecss/unpack/fs';

const here = dirname(fileURLToPath(import.meta.url));
const FONTS_DIR = resolve(here, '../src/fonts');
const OUT_FILE = resolve(here, '../src/globals/font-fallbacks.generated.css');

const FONTS = [
  {
    family: 'IBM Plex Sans Condensed',
    fallbackName: 'IBM Plex Sans Condensed Fallback',
    fallback: lookup('Arial'),
    weights: [400, 500, 600, 700],
    filePrefix: 'ibm-plex-sans-condensed',
  },
  {
    family: 'IBM Plex Mono',
    fallbackName: 'IBM Plex Mono Fallback',
    fallback: lookup('Courier New'),
    weights: [400, 500, 600],
    filePrefix: 'ibm-plex-mono',
  },
];

function lookup(name) {
  return Object.values(entireMetricsCollection).find((m) => m.familyName === name);
}

function calc(primary, fb) {
  // Chrome fallback formula — both ratios normalized by their own unitsPerEm before dividing.
  // Skipping UPM normalization mis-scales across fonts with different em squares (e.g. Plex 1000, Arial 2048).
  const primaryRatio = primary.xWidthAvg / primary.unitsPerEm;
  const fbRatio = fb.xWidthAvg / fb.unitsPerEm;
  const sizeAdjust = primaryRatio / fbRatio;
  const scaled = primary.unitsPerEm * sizeAdjust;
  const pct = (n) => `${((n / scaled) * 100).toFixed(4)}%`;
  return {
    sizeAdjust: `${(sizeAdjust * 100).toFixed(4)}%`,
    ascent: pct(primary.ascent),
    descent: pct(Math.abs(primary.descent)),
    lineGap: pct(primary.lineGap),
  };
}

async function main() {
  const blocks = [
    `/* Auto-generated by scripts/gen-font-fallbacks.mjs — do not edit by hand. */`,
    `/* Metric-overridden fallbacks neutralize CLS during font swap (Chrome font-fallbacks doctrine). */`,
    '',
  ];
  for (const spec of FONTS) {
    if (!spec.fallback) {
      throw new Error(`No capsize metrics for fallback "${spec.fallbackName}".`);
    }
    for (const weight of spec.weights) {
      const path = `${FONTS_DIR}/${spec.filePrefix}-${weight}-latin.woff2`;
      const m = await fromFile(path);
      const { sizeAdjust, ascent, descent, lineGap } = calc(m, spec.fallback);
      blocks.push(
        `@font-face {`,
        `  font-family: "${spec.fallbackName}";`,
        `  src: local("${spec.fallback.familyName}");`,
        `  font-weight: ${weight};`,
        `  font-style: normal;`,
        `  size-adjust: ${sizeAdjust};`,
        `  ascent-override: ${ascent};`,
        `  descent-override: ${descent};`,
        `  line-gap-override: ${lineGap};`,
        `}`,
        '',
      );
    }
  }
  await writeFile(OUT_FILE, blocks.join('\n'), 'utf8');
  console.log(`wrote ${OUT_FILE}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
